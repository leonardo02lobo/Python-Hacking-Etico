# Listas de objetos
fruits = ["apple","banana","cherry"]

# Bucle for tradicional para listas 
for fruit in fruits:
    print(fruit)

# Bucles for con rangos 
for number in range(1,10):
    print(number)
# Bucle for con rango 
# Imprime los numeros de 2 en 2
for number in range(1,100,2):
    print(number)